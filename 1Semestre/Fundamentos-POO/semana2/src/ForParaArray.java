public class ForParaArray {
    public static void main(String[] args) {
        int [] vet = new int[] {10,2,30,-5};

        for (int i6 = 0; i6 < 4; i6++) {
            // Imprime o conteúdo do vetor na posição i
            System.out.print(vet[i6] + " ");
        }
        System.out.println("\nfim 6");
    }

}
